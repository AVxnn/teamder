const bgMap: Record<string, string> = {
  waiting: 'bg-gradient-to-r from-[#140A0A] to-[#36663F]',
  like: 'bg-gradient-to-r from-[#140A0A] to-[#a94a4a]',
  superlike: 'bg-gradient-to-r from-[#140A0A] to-[#a98d4a]',
  'moderation-fail': 'bg-gradient-to-r from-[#140A0A] to-[#813030]',
  'moderation-success': 'bg-gradient-to-r from-[#140A0A] to-[#3A4070]',
  'profile-deleted': 'bg-gradient-to-r from-[#140A0A] to-[#363636]',
};

export type NotificationType =
  | 'waiting'
  | 'like'
  | 'superlike'
  | 'moderation-success'
  | 'moderation-fail'
  | 'profile-deleted';

interface NotificationCardProps {
  type: NotificationType;
  user?: { name: string; avatar: string };
  title: string;
  message?: string;
}

export default function NotificationCard({
  type,
  user,
  title,
  message,
}: NotificationCardProps) {
  return (
    <div
      className={`flex items-center rounded-3xl outline outline-[#363636] !px-4 !py-5 !mb-3 ${bgMap[type]} relative`}
    >
      {user ? (
        <img
          src={user.avatar}
          alt={user.name}
          className="w-12 h-12 rounded-full object-cover !mr-4 border-2 border-[#363636]"
        />
      ) : (
        <>
          {type === 'waiting' ||
          type === 'moderation-success' ||
          type === 'moderation-fail' ||
          type === 'profile-deleted' ? (
            <div className="!mr-4 flex-shrink-0">{icons[type]}</div>
          ) : null}
        </>
      )}

      <div className="flex-1 min-w-0">
        <div className="text-base font-semibold text-white truncate">
          {user ? user.name : title}
          {type === 'waiting' ||
          type === 'moderation-success' ||
          type === 'moderation-fail' ||
          type === 'profile-deleted'
            ? 'Ваша карточка'
            : null}
        </div>
        <div className={`text-sm text-[#AFAFAF] truncate`}>
          {user ? title : message}
        </div>
        {!user && !message && (
          <div className="text-sm text-white/80 truncate">{title}</div>
        )}
      </div>

      {type === 'like' || type === 'superlike' ? (
        <div className="!ml-4 flex-shrink-0">{icons[type]}</div>
      ) : null}
    </div>
  );
}
